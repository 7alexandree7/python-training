def somar_2_numeros(num1, num2):
    
    soma = (num1 + num2)
    return soma
  
    
while True:
    num1 = int(input("Dite o primeiro Numero: "))
    num2 = int(input("Digite o segundo Numero: "))       
    resposta = somar_2_numeros(num1, num2)
    print(f"resposta {resposta}")
        
        
        


